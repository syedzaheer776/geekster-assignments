
public class loops {

	public static void main(String[] args) {
		System.out.println("print no.s using while loop");
		int i=1;
		while(i<=10)
		{
			System.out.println(i);
			i++;
		}
		System.out.println("print no.s in reverse order");
		int j=10;
		while(j>=1)
		{
			System.out.println(j);
			j--;
		}

	}

}
